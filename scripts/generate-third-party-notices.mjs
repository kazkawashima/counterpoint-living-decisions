import { readFile, rename, writeFile } from "node:fs/promises";
import { resolve } from "node:path";
import { fileURLToPath, pathToFileURL } from "node:url";

const REVIEW_REQUIRED_LICENSE =
  /(?:^|[^A-Z])(?:A?GPL|LGPL|MPL|EPL|CDDL)(?:[^A-Z]|$)/iu;

export function assertThirdPartyNoticeCurrent(lockfile, currentNotice) {
  const expected = renderThirdPartyNotices(collectThirdPartyPackages(lockfile));
  if (currentNotice !== expected) {
    throw new Error(
      "THIRD_PARTY_NOTICES.md is stale; run npm run licenses:generate",
    );
  }
}

function packageName(path) {
  return path.split("node_modules/").at(-1);
}

export function collectThirdPartyPackages(lockfile) {
  const runtimeDirectPaths = new Set();
  const developmentDirectPaths = new Set();
  for (const [path, metadata] of Object.entries(lockfile.packages ?? {})) {
    if (path.includes("node_modules/") || metadata.link === true) {
      continue;
    }
    for (const name of Object.keys(metadata.dependencies ?? {})) {
      runtimeDirectPaths.add(`node_modules/${name}`);
      if (path.length > 0) {
        runtimeDirectPaths.add(`${path}/node_modules/${name}`);
      }
    }
    for (const name of Object.keys(metadata.devDependencies ?? {})) {
      developmentDirectPaths.add(`node_modules/${name}`);
      if (path.length > 0) {
        developmentDirectPaths.add(`${path}/node_modules/${name}`);
      }
    }
  }
  const packages = [];

  for (const [path, metadata] of Object.entries(lockfile.packages ?? {})) {
    if (!path.includes("node_modules/") || metadata.link === true) {
      continue;
    }
    const name = packageName(path);
    const version = metadata.version;
    if (typeof name !== "string" || typeof version !== "string") {
      throw new TypeError(`Invalid package-lock entry: ${path}`);
    }
    if (typeof metadata.license !== "string" || metadata.license.length === 0) {
      throw new TypeError(`${name}@${version} is missing license metadata`);
    }
    const relationship = runtimeDirectPaths.has(path)
      ? "runtime direct"
      : developmentDirectPaths.has(path)
        ? "development direct"
        : metadata.dev === true
          ? "development transitive"
          : "runtime transitive";
    packages.push({
      license: metadata.license,
      name,
      relationship,
      version,
    });
  }

  return [
    ...new Map(
      packages.map((entry) => [
        [entry.name, entry.version, entry.license, entry.relationship].join(
          "\0",
        ),
        entry,
      ]),
    ).values(),
  ].sort(
    (left, right) =>
      left.name.localeCompare(right.name) ||
      left.version.localeCompare(right.version) ||
      left.relationship.localeCompare(right.relationship),
  );
}

export function renderThirdPartyNotices(packages) {
  const reviewRequired = packages.filter(({ license }) =>
    REVIEW_REQUIRED_LICENSE.test(license),
  );
  const reviewSummary =
    reviewRequired.length === 0
      ? "No reciprocal or file-level copyleft license identifier was found in the lockfile metadata."
      : reviewRequired.every(({ relationship }) =>
            relationship.startsWith("development"),
          )
        ? "Review-required licenses are present only in development dependencies; they are not application runtime dependencies."
        : "Review-required licenses are present in runtime dependencies and require release-specific legal review.";
  const rows = packages
    .map(
      ({ license, name, relationship, version }) =>
        `| ${name} | ${version} | ${license} | ${relationship} |`,
    )
    .join("\n");

  return `# Third-party notices

Generated from \`package-lock.json\`; do not edit this file manually.

This inventory records JavaScript package metadata for submission and release
review. It does not change the license of Counterpoint source code and is not
legal advice. Package authors' license files remain authoritative.

${reviewSummary}

Non-code files under \`docs/media/\` are tracked separately from this JavaScript
package inventory and require their own asset manifest before public release.

| Package | Version | License | Relationship |
| --- | --- | --- | --- |
${rows}
`;
}

const invokedAsScript =
  process.argv[1] !== undefined &&
  import.meta.url === pathToFileURL(resolve(process.argv[1])).href;

if (invokedAsScript) {
  const repositoryRoot = fileURLToPath(new URL("../", import.meta.url));
  const lockfilePath = resolve(repositoryRoot, "package-lock.json");
  const outputPath = resolve(repositoryRoot, "THIRD_PARTY_NOTICES.md");
  const lockfile = JSON.parse(await readFile(lockfilePath, "utf8"));
  if (process.argv[2] === "--check") {
    assertThirdPartyNoticeCurrent(lockfile, await readFile(outputPath, "utf8"));
    console.log("THIRD_PARTY_NOTICES.md is current");
  } else {
    const notice = renderThirdPartyNotices(collectThirdPartyPackages(lockfile));
    const temporaryPath = `${outputPath}.tmp`;
    await writeFile(temporaryPath, notice);
    await rename(temporaryPath, outputPath);
    console.log(`Wrote ${outputPath}`);
  }
}
